package utilitaire;

public class TreeTaggerToken {
	public String token;
	public String pos;
	public String lemma;
	/**
	 * @param token
	 * @param pos
	 * @param lemma
	 */
	public TreeTaggerToken(String token, String pos, String lemma) {
		super();
		this.token = token;
		this.pos = pos;
		this.lemma = lemma;
	}
	
	
}
